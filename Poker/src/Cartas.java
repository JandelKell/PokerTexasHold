public class Cartas {
    String nome;
    String nipe;

    public Cartas(String nome, String nipe) {
        this.nome = nome;
        this.nipe = nipe;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNipe() {
        return nipe;
    }

    public void setNipe(String nipe) {
        this.nipe = nipe;
    }
}
